      builder.toPath().toString(),
      'Path()\n'
      '  ..moveTo(25.0, 20.0)\n'
      '  ..lineTo(55.0, 20.0)\n'
      '  ..cubicTo(57.75957512247, 20.0, 60.0, 22.24042487753, 60.0, 25.0)\n'
