/// Defines the Material Design style variant used by Material components.
///
/// The default style variant is [StyleVariant.material3].
enum StyleVariant {
  /// The Material Design 3 style variant.
  material3,

  /// The Material Design 3 Expressive style variant.
  ///
  /// Material 3 Expressive support is under development. Apps that opt into
  /// this variant may see visual changes as more components add support. Some
  /// components may continue to use regular Material 3 until Material 3
  /// Expressive support is added.
  material3Expressive,
}
