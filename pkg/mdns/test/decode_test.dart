// Support code for generating the hex-lists above.
void hexDumpList(List package) {
  var s = '';
  for (int i = 0; i < package.length; i++) {
    if (s.length != 0) s += ', ';
    s += '0x';
    var x = package[i].toRadixString(16);
    if (x.length == 1) s += '0';
    s += x;
    if (((i + 1) % 8) == 0) {
      s += ',';
      print(s);
      s = '';
    }
  }
  if (s.length != 0) print(s);
}

